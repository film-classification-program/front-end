              </div>
              
              <div class="info-row">
                <span class="info-icon">⏱️</span>
                <span class="info-label">片长:</span>
                <span class="info-value">{{ result.duration }}</span>
              </div>
              
              <div class="info-row">
                <span class="info-icon">🌐</span>
                <span class="info-label">语言:</span>
                <span class="info-value">{{ result.language }}</span>
              </div>
              
              <div class="info-row">
                <span class="info-icon">🎞️</span>
                <span class="info-label">IMDb:</span>
                <span class="info-value">{{ result.imdb }}</span>
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import FilmLogo from './FilmLogo.vue'
import SearchBar from './SearchBar.vue'
import { searchFilms } from '../services/filmApi'

export default {
  name: 'SearchResults',
  components: {
    FilmLogo,
    SearchBar
  },
  props: {
    query: {
      type: String,
      default: ''
    }
  },
  data() {
    return {
      searchPlaceholder: '你想找什么样的电影？',
      searchResults: [],
      searchResultsLoaded: false
    }
  },
  created() {
    // 在组件创建时加载数据
    this.fetchResults()
  },
  watch: {
    // 监听查询参数变化，重新获取结果
    query(newValue) {
      if (newValue) {
        this.fetchResults()
      }
    }
  },
  methods: {
    performSearch(query) {
      if (query.trim() !== '') {
        this.$router.replace({
          path: '/search',
          query: { desc: query }
        })
      }
    },
    async fetchResults() {
      this.searchResultsLoaded = false
      try {
        // 使用API服务获取结果
        this.searchResults = await searchFilms(this.query)
      } catch (error) {
        console.error('搜索失败:', error)
        this.searchResults = []
      } finally {
        this.searchResultsLoaded = true
      }
    }
  }
}
</script>

